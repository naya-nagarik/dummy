/**
 * WAP to calculate sum of any two numbers and print result.
 */

#include <stdio.h>

int main()
{
    // Declare variables
    int a = 13;
    int b = 11;

    // Calculate the sum of 'a' and 'b'
    int sum = a + b;

    // Print the sum
    printf("Sum of %d and %d is %d.\n", a, b, sum);

    // Wait for user to press Enter
    printf("\nPress Enter to continue...");
    getchar();

    return 0;
}
